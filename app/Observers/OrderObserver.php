    }

    /**
     * Handle the Order "deleted" event.
     */
    public function deleted(Order $order): void
    {
        foreach ($order->order_items as $order_item) {
            $product = Product::find($order_item->product_id);
            $product->amount += $order_item->amount;
            $product->save();

            $order_item->delete();
        }
    }

